package com.cmile.serviceutil.executor;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.task.TaskExecutor;
import org.springframework.scheduling.concurrent.ThreadPoolTaskExecutor;

@Configuration
public class CfgTaskExecutor {

    @Value("${cmile.cache.hydrate.core.pool.size:1}")
    private int corePoolSize;

    @Value("${cmile.cache.hydrate.max.pool.size:5}")
    private int maxPoolSize;

    @Value("${cmile.cache.hydrate.queue.capacity:10000}")
    private int queueCapacity;

    @Bean("cacheTaskExecutor")
    public TaskExecutor taskExecutor() {
        ThreadPoolTaskExecutor executor = new ThreadPoolTaskExecutor();
        executor.setCorePoolSize(corePoolSize);
        executor.setMaxPoolSize(maxPoolSize);
        executor.setQueueCapacity(queueCapacity);
        executor.setThreadNamePrefix("CacheExecutor-");
        executor.initialize();
        return executor;
    }
}
